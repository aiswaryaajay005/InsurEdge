        <h2>Understanding Insurance Types and Their Coverage</h2>
        <p>Insurance is a financial product designed to help protect you from financial loss or hardship. There are many types of insurance, each serving a different purpose, and each with varying coverage options. This page will help you understand the different types of insurance, their features, and when and how they pay out.</p>

        <!-- Health Insurance Section -->
        <div class="insurance-type">
            <h3>1. Health Insurance</h3>
            <p>Health insurance is designed to cover medical expenses, including hospital bills, doctor visits, surgeries, and sometimes even preventive care. With the rising costs of healthcare, it’s essential to have a plan that covers your medical needs.</p>
            <h4>Types of Health Insurance:</h4>
            <ul>
                <li><strong>Individual Health Insurance:</strong> Covers only the policyholder’s medical expenses.</li>
                <li><strong>Family Health Insurance:</strong> Covers the policyholder, spouse, children, and sometimes other family members under one plan.</li>
                <li><strong>Critical Illness Insurance:</strong> Provides coverage for major illnesses such as cancer, heart attacks, strokes, and kidney failure. It often pays a lump sum amount to the policyholder.</li>
                <li><strong>Group Health Insurance:</strong> Usually offered by employers to cover a group of employees and their families. It may offer more affordable premiums compared to individual plans.</li>
            </ul>
            <h4>When is the Full Amount Paid?</h4>
            <p>Health insurance generally does not provide a full payout unless it covers specific treatments or hospital stays as per the policy limits. The amount reimbursed depends on your plan’s coverage and the type of medical expenses incurred. Policies may pay a percentage (e.g., 80% of medical costs) while the remaining amount may be your responsibility (coinsurance). Some plans also have deductibles that must be paid before insurance kicks in.</p>
            <h4>Key Benefits:</h4>
            <ul>
                <li>Covers medical treatment expenses, including hospitalization, medications, and doctor visits.</li>
                <li>Critical illness plans provide lump sum payouts to the policyholder if diagnosed with a covered condition.</li>
                <li>Some health plans may include wellness programs and preventive care to maintain overall health.</li>
            </ul>
        </div>
        
        <!-- Life Insurance Section -->
        <div class="insurance-type">
            <h3>2. Life Insurance</h3>
            <p>Life insurance helps ensure that your loved ones are financially secure after your passing. It pays out a lump sum amount (known as the death benefit) to your beneficiaries when you die. This payout can help cover living expenses, funeral costs, and outstanding debts.</p>
            <h4>Types of Life Insurance:</h4>
            <ul>
                <li><strong>Term Life Insurance:</strong> Provides coverage for a fixed period, typically 10, 20, or 30 years. If the policyholder dies within the term, the beneficiaries receive the sum assured. If the policyholder survives the term, no payout is made.</li>
                <li><strong>Whole Life Insurance:</strong> Offers coverage for the entire lifetime of the policyholder. It also has a cash value component that grows over time, which the policyholder can borrow against.</li>
                <li><strong>Endowment Life Insurance:</strong> A combination of life insurance and a savings plan. This type of policy provides a death benefit if the policyholder passes away, or a lump sum payout at the end of the policy term if they survive.</li>
                <li><strong>Unit-Linked Insurance Plans (ULIPs):</strong> Combines life insurance with investment. A portion of your premium is invested in stocks, bonds, or other securities, and the death benefit depends on the performance of the investment.</li>
            </ul>
            <h4>When is the Full Amount Paid?</h4>
            <p>Term life insurance pays the full sum assured to your beneficiaries if you die within the policy term. Whole life insurance and endowment plans will pay out the death benefit or the maturity amount (if you outlive the policy term). For ULIPs, the death benefit depends on the performance of your investment portfolio.</p>
            <h4>Key Benefits:</h4>
            <ul>
                <li>Provides financial security to your family after your passing.</li>
                <li>Whole life and endowment plans accumulate cash value over time, offering additional financial flexibility.</li>
                <li>Term life insurance is typically more affordable than other types of life insurance.</li>
            </ul>
        </div>
        
        <!-- Vehicle Insurance Section -->
        <div class="insurance-type">
            <h3>3. Vehicle Insurance</h3>
            <p>Vehicle insurance is designed to protect your vehicle from damage, theft, or accidents. Depending on the type of insurance, it may cover third-party damage, your vehicle’s damage, or both.</p>
            <h4>Types of Vehicle Insurance:</h4>
            <ul>
                <li><strong>Comprehensive Insurance:</strong> Covers damage to your own vehicle as well as third-party liabilities, including property damage, injury, or death to other people in an accident.</li>
                <li><strong>Third-Party Liability Insurance:</strong> Covers damage or injury caused to others in an accident. This type of insurance does not cover damage to your own vehicle.</li>
                <li><strong>Own Damage Insurance:</strong> Covers damage to your vehicle, but it does not cover third-party liabilities.</li>
                <li><strong>Third-Party Fire and Theft Insurance:</strong> A basic policy that covers third-party damages as well as damage to your vehicle from fire or theft.</li>
            </ul>
            <h4>When is the Full Amount Paid?</h4>
            <p>Comprehensive and own damage policies pay out based on the repair cost or the current market value of the vehicle (minus depreciation). If your vehicle is written off (total loss), the policy will pay out the insured value after deducting depreciation. Third-party insurance only covers the liability for damages caused to others.</p>
            <h4>Key Benefits:</h4>
            <ul>
                <li>Helps repair or replace your vehicle in case of accidents or damage.</li>
                <li>Comprehensive policies cover both your vehicle and third-party damages.</li>
                <li>Required by law in many countries for driving on the road (third-party liability). </li>
            </ul>
        </div>
        
        <!-- Home Insurance Section -->
        <div class="insurance-type">
            <h3>4. Home Insurance</h3>
            <p>Home insurance protects your house and belongings from damages caused by natural calamities, theft, fire, or vandalism. It typically covers the structure of your home, personal property, and sometimes additional structures like garages or sheds.</p>
            <h4>Types of Home Insurance:</h4>
            <ul>
                <li><strong>Building Insurance:</strong> Covers the physical structure of your home against perils like fire, flooding, or earthquakes.</li>
                <li><strong>Contents Insurance:</strong> Covers the personal belongings inside your home, such as furniture, electronics, and valuables, against loss or damage.</li>
                <li><strong>Comprehensive Home Insurance:</strong> Offers both building and contents coverage, providing full protection for your home and personal belongings.</li>
            </ul>
            <h4>When is the Full Amount Paid?</h4>
            <p>If your home is completely destroyed by a covered peril, home insurance will pay out the cost of rebuilding or repairing it (subject to the policy limits and deductibles). Contents insurance will cover the replacement cost or the market value of lost or damaged belongings.</p>
            <h4>Key Benefits:</h4>
            <ul>
                <li>Helps protect your most valuable asset—your home—against unexpected events.</li>
                <li>Covers personal belongings, which can be especially important in case of burglary or fire.</li>
                <li>Offers peace of mind knowing you have financial protection if something happens to your home.</li>
            </ul>
        </div>

        <h3>Which Policy Should You Choose?</h3>
        <p>The right insurance for you will depend on your personal circumstances. Consider the following factors:</p>
        <ul>
            <li>If you have dependents and want to provide for them after your passing, <strong>life insurance</strong> is essential.</li>
            <li>If you are concerned about unexpected medical expenses, <strong>health insurance</strong> will offer the protection you need.</li>
            <li>If you own a home or vehicle, <strong>home insurance</strong> or <strong>vehicle insurance</strong> will safeguard your property from damage or theft.</li>
            <li>If you’re planning a trip, don’t forget to secure <strong>travel insurance</strong> for added peace of mind.</li>
        </ul>
        <p>Always evaluate your specific needs, budget, and risk factors before choosing a policy. Consulting with an insurance advisor can also help you make a more informed decision.</p>
